package kakaologin;

public class KakaoLoginVO {
	private String kakao_email, kakao_nickname, kakao_profile;

	public String getKakao_email() {
		return kakao_email;
	}

	public void setKakao_email(String kakao_email) {
		this.kakao_email = kakao_email;
	}

	public String getKakao_nickname() {
		return kakao_nickname;
	}

	public void setKakao_nickname(String kakao_nickname) {
		this.kakao_nickname = kakao_nickname;
	}

	public String getKakao_profile() {
		return kakao_profile;
	}

	public void setKakao_profile(String kakao_profile) {
		this.kakao_profile = kakao_profile;
	}
	
	

}
